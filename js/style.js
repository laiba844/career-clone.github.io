function checkbox(){
    let fb=document.getElementById('fb').value;
    let fb_control=document.getElementById('fb_control');
    console.log(fb);
    if(fb == "fb"){
        fb_control.style.display='block'
    }
    else{
        fb_control.style.display='none'
    }
    
}
